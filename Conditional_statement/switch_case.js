var grade = 'D';
switch (grade) {
    case 'A': console.log("Great Job");
        break;
    case 'B': console.log("Pretty good");
        break;
    case 'C': console.log("Passed");
        break;
    case 'D': console.log("Just Passed");
        break;
    case 'F': console.log("Failed");
        break;
    default: console.log("Unknown Grade");
}